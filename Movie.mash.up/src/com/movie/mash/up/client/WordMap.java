    public String purgeNegativeWords(String input)
    {
    	StringBuffer output = new StringBuffer();	
    	List<String> result = this.splitWords(input);
    	for ( String word : result )
    	{
    		if ( !this.negativelist.containsKey(word) )
    			output.append(word + _space);
    	}
    	return output.toString();
    }  

   
