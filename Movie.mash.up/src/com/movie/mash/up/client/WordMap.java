    public WordMap()
    {
    	
        this.stoplist = new Hashtable<String, Boolean>(); //remove the stop words
        this.negativelist = new Hashtable<String, Boolean>(); //build a map of negative words
        this.positivelist = new Hashtable<String, Boolean>(); //build a map of positive words
        
        
        this.addStandardStopWords();
        this.addNegativeStopWords();
        this.addPositiveStopWords();
        return;
    }
    
    

    /**
     *  Split words based on white space
     */
    private List<String> splitWords(String input)
    {
    	List<String> resultSet = new Vector<String>();
    	
    	String result[] = input.split(_splitSpace);
    	// remove any punctuation (other than apostrophe) from word
    	// and change to all lower case
    	for ( String word : result )
    	{
    		word = word.replaceAll("[.,!?:;]", "");
    		word = word.toLowerCase();
    		resultSet.add(word);
    	}
    	return resultSet;
    }
    
   
