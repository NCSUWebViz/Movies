package com.movie.mash.up.server.scrapping;

public class RottenTomatoesJsonMovie {

	private String id;
	private String title;
	private RottenTomatoesJsonMovieRating ratings;
	
	public RottenTomatoesJsonMovieRating getRatings() {
		return ratings;
	}
	public void setRatings(RottenTomatoesJsonMovieRating ratings) {
		this.ratings = ratings;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
}
